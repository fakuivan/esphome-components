def parse_software_version(value: str) -> bytes:
    normalized = value.replace(" ", "")
    if len(normalized) != 10:
        raise argparse.ArgumentTypeError(
            "software version must be exactly 10 hex digits"
        )
    try:
        return bytes.fromhex(normalized)
    except ValueError as exc:
        raise argparse.ArgumentTypeError("software version must be valid hex") from exc


def main() -> int:
    parser = build_arg_parser()
    args = parser.parse_args()

    if not 0 <= args.address <= 0xFF:
        parser.error("address must fit in one byte")
    if args.module_count < 1:
        parser.error("module-count must be at least 1")
    if args.address + args.module_count - 1 > 0xFF:
        parser.error("address + module-count exceeds one-byte address range")
    if not 0 <= args.protocol_version <= 0xFF:
        parser.error("protocol version must fit in one byte")
    if not 0 <= args.soc <= 100:
        parser.error("soc must be between 0 and 100")
    if not 0 <= args.soh <= 100:
        parser.error("soh must be between 0 and 100")
    if args.total_capacity > 0xFFFF:
        parser.error("total-capacity must be 65.535 Ah or less for the simple response format")

    profile = BatteryProfile(
        base_address=args.address,
        module_count=args.module_count,
        cell_count=DEFAULT_CELL_COUNT,
        module_voltage_mv=args.module_voltage,
        charge_voltage_mv=args.charge_voltage,
        low_cutoff_voltage_mv=args.low_cutoff_voltage,
        max_charge_current_da=args.max_charge_current,
        max_discharge_current_da=args.max_discharge_current,
        total_capacity_mah=args.total_capacity,
        remaining_capacity_mah=args.total_capacity,
        current_da=args.current,
        cycle_count=args.cycle_count,
        state_of_charge=args.soc,
        state_of_health=args.soh,
        temperature_deci_c=args.temperature,
    )

    logging.basicConfig(
        level=getattr(logging, args.log_level),
        format="%(asctime)s %(levelname)s %(message)s",
    )

    serial_port = serial.Serial(
        port=args.port,
        baudrate=args.baudrate,
        bytesize=serial.EIGHTBITS,
        parity=serial.PARITY_NONE,
        stopbits=serial.STOPBITS_ONE,
        timeout=0.2,
        write_timeout=0.5,
        exclusive=True,
    )

    responder = VersionResponder(
        serial_port,
        profile=profile,
        protocol_version=args.protocol_version,
        software_version=args.software_version,
        reply_delay=args.reply_delay_ms / 1000.0,
    )

    signal.signal(signal.SIGINT, responder.stop)
    signal.signal(signal.SIGTERM, responder.stop)

    logging.info(
        "Listening on %s at %d baud for base address 0x%02X",
        args.port,
        args.baudrate,
        args.address,
    )
    logging.info(
        "Emulating module addresses 0x%02X-0x%02X at %.3f V, SOC %d%%",
        profile.base_address,
        profile.base_address + profile.module_count - 1,
        profile.module_voltage_mv / 1000.0,
        profile.state_of_charge,
    )
    if args.software_version is None:
        logging.info("0x96 replies disabled")
    else:
        logging.info(
            "0x96 replies enabled with payload %s", args.software_version.hex().upper()
        )

    try:
        responder.run()
    finally:
        serial_port.close()

    return 0


if __name__ == "__main__":
    sys.exit(main())
